#include <stdio.h>
#include <signal.h>
#include <stdlib.h>
#include <string.h>
#include <unistd.h>

void banner ();

void ignore () {
  alarm(30);
  setvbuf(stdout, NULL, _IONBF, 0);
  setvbuf(stdin, NULL, _IONBF, 0);
  setvbuf(stderr, NULL, _IONBF, 0);
}

void win () {
  system("/bin/sh");
}

void challenge () {
  const int size = 128;
  char buffer[size];
  
  printf(
    "Hey. I'm the One (%p) who stands between you and your prize!\n"
    "Have you got what it takes to win? (y/n)\n"
    ">>> ",
    win
  );
  fflush(stdout);
  fgets(buffer, 256, stdin);
}

int main () {
  ignore();
  banner();
  challenge();

  puts("You failed!");

  return 0;
}

